void lcd_character(uint8_t x, uint8_t y, uint8_t ch, uint16_t colorf, uint16_t colorb){
	// Bounds check
	if(ch > 127)
		return;

	lcd_window(x, y, 8, 8);
	
	
	
	uint8_t* bitmap = font8x8_basic[ch];
	for (int yi=0; yi < 8; ++yi) {
		for (int xi=0; xi < 8; ++xi)
			if(bitmap[yi] & (1 << xi)){
				lcd_transfer16(1, colorf);
				//lcd_data(colorf >> 8); // RGB565 Upper
				//lcd_data(colorf); // RGB565 Lower
			}else{
				lcd_transfer16(1, colorb);
				//lcd_data(colorb >> 8); // RGB565 Upper
				//lcd_data(colorb); // RGB565 Lower
			}
    }
}

uint8_t cursor_x;
uint8_t cursor_y;

void lcd_print_rgb(uint8_t* ch, uint16_t colorf, uint16_t colorb){
	unsigned int i = 0;
	uint8_t x_bound = cursor_x;
	
	while(ch[i] != '\0'){
		if(ch[i] == '\n'){
			cursor_y += 8;
			cursor_x = x_bound;
			i++;
			continue;
		}
		lcd_character(cursor_x, cursor_y, ch[i], colorf, colorb);
		i++;
		cursor_x += 8;
	}
}

void lcd_println_rgb(uint8_t* ch, uint16_t colorf, uint16_t colorb){
	uint8_t x_bound = cursor_x;
	lcd_print_rgb(ch, colorf, colorb);
	cursor_y += 8;
	cursor_x = x_bound;
}

void lcd_print(uint8_t* ch){
	lcd_print_rgb(ch, COLOR_WHITE, COLOR_BLACK);
}

void lcd_println(uint8_t* ch){
	lcd_println_rgb(ch, COLOR_WHITE, COLOR_BLACK);
}

void lcd_cursor(uint8_t x, uint8_t y){
	cursor_x = x;
	cursor_y = y;
}

void lcd_bitmap(uint8_t x, uint8_t y, uint16_t* bitmap, uint8_t width, uint8_t height){
	lcd_window(x, y, width, height);
	
	for(int i = 0; i < width*height; ++i){
		lcd_transfer16(1, bitmap[i]);
	}
}

void lcd_bitmap_alpha(uint8_t x, uint8_t y, uint16_t* bitmap, uint8_t width, uint8_t height, uint16_t alphacolor){
	for(int i = 0; i < width*height; ++i){
		if(bitmap[i] == alphacolor)
			continue;
		lcd_pixel(x + i%width, y + i/width, bitmap[i]);  
	}
}
